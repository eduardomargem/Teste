    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        String tipoComputador = "";
        if(rboPc.isSelected()){
            tipoComputador = rboPc.getText();
        }else if(rboNotebook.isSelected()){
            tipoComputador = rboNotebook.getText();
        }else if (rboServidor.isSelected()){
            tipoComputador = rboServidor.getText();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Selecione um tipo de computador: ");
            return;
        }
        //RESGATANDO O VALOR SELECIONADO DO SISTEMA OPERACIONAL
        String sistema = "";
        sistema = grupoSO.getSelection().getActionCommand();
        String tipoHD = "";
        if(cboHD.getSelectedIndex() >0){
            tipoHD = cboHD.getSelectedItem().toString();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Selecione o HD!");
        }
        
        String acessorios = "";
        if(chkMochila.isSelected()){
            acessorios += chkMochila.getText() + " ,";
        }
        if(chkHeadset.isSelected()){
            acessorios += chkHeadset.getText() + " ,";
        }
        if(chkMouse.isSelected()){
            acessorios += chkMouse.getText() + " ,";
        }
        
        String garantiasSelecionadas = "";
        List <String> garantias = lstGarantias.getSelectedValuesList();
        // PARA CADA ITEM NA LISTA DE GARANTIAS, FA√áO ALGUMA COISA
        for(String item: garantias){
            garantiasSelecionadas += item + " ,";
        }
        //ADICIONAR OS ITENS SELECIONADOS A TABELA PRODUTOS
        //ACESSO A CAMADA MODEL PARA ADICIONAR OU REMOVER LINHAS E COLUNAS
        DefaultTableModel modelo = (DefaultTableModel) tblProdutos.getModel();
        //ADIDIONO AS OP√áOES A CAMADA DE MODELAGEM DA TABELA
        modelo.addRow(new String[]{tipoComputador, sistema, tipoHD, acessorios, garantiasSelecionadas});
        
        JOptionPane.showMessageDialog(rootPane, tipoComputador+ "\n"+sistema+"\n"+ tipoHD);
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
       //VERIFICAR SE POSSUEM LINHAS SELECIONADAS
       int  linhaSelecionada = tblProdutos.getSelectedRow();
       //-1 √© o valor quando n√£o seleciono nada
       if(linhaSelecionada == -1){
           JOptionPane.showMessageDialog(rootPane, "Selecione uma linha!");
       }else{
           DefaultTableModel modelo = (DefaultTableModel) tblProdutos.getModel();
           modelo.removeRow(linhaSelecionada);
       }
    }//GEN-LAST:event_btnExcluirActionPerformed

